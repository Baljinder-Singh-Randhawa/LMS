        <h2>THE BRITISH LIBRARY</h2></i>
        <i><p>The British Library, founded in 1753, contained one of the world’s largest collections of books, manuscripts and periodicals, 
            both contemporary and antique, British and foreign. <br><br>

It was created as ‘one general repository’ to hold the collections of Sir Hans Sloane, Sir Robert Cotton and Robert and Edward Harley. When it 
inherited the library of George III in 1823, its printed books doubled in number, prompting a move to the site of the current British Museum. <br><br>

Opening in 1857, the Library’s Round Reading Room – with its magnificent domed roof – became an iconic destination in the literary landscape of
 London. George Gissing used it as the setting of his 1891 novel New Grub Street, describing it as ‘the valley of the shadow of books’, while
  lamenting the difficulties of obtaining a Reader ticket. Its roof was also used in the climax of Alfred Hitchcock’s film Blackmail (1929). <br><br>

The room welcomed many famous visitors including Charles Darwin, Charles Dickens, Karl Marx, George Bernard Shaw and Virginia Woolf. Lenin too
 applied for a Reader ticket, adopting the pseudonym Jacob Richter to cover his tracks from the Russian authorities. <br><br>

During World War II, some of our most precious treasures were moved to a secure cave in Aberystwyth, with round the clock guards. Meanwhile, 
the Newspaper Library in Colindale, north London, suffered substantial damage from bombing and some of the collection had to be transferred to 
quarries in Wiltshire while repairs were made.</p></i> <br><br>
